	public int getpeso(String loc1,String loc2){
		String sql = "SELECT	count(DISTINCT (i.`Type`)) as c "
				+ "from interactions i, classification c1,classification c2 "
				+ "WHERE i.GeneID1=c1.GeneID AND i.GeneID2=c2.GeneID AND c1.Localization<>c2.Localization AND ((c1.Localization=? "
				+ "AND c2.Localization=? ) OR (c1.Localization=? "
				+ "AND c2.Localization=?  )    ) ";
		int result=0;
		Connection conn = DBConnect.getConnection();

		try {
			PreparedStatement st = conn.prepareStatement(sql);
			st.setString(1, loc1);
			st.setString(2, loc2);
			st.setString(3, loc2);
			st.setString(4, loc1);
			ResultSet res = st.executeQuery();
			while (res.next()) {

				result=res.getInt("c");
			}
			res.close();
			st.close();
			conn.close();
			return result;
			
		} catch (SQLException e) {
			throw new RuntimeException("Database error", e) ;
		}
	}


	
